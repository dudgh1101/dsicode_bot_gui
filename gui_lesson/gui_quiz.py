"""
================================================================================
【카운터 예제】
================================================================================
이 프로그램은 Tkinter를 사용해 간단한 GUI 앱을 만듭니다.
- 목표: 버튼을 클릭할 때마다 숫자가 증가하는 UI 만들기
- 핵심 개념: 윈도우, 위젯(Widget), 이벤트(Event), 콜백(Callback)
================================================================================
"""

import tkinter as tk


# ─────────────────────────────────────────────────────────────────────────────
# 【클래스 정의: CounterApp】
# 목적: 카운터 앱의 모든 UI와 기능을 하나의 클래스로 관리
# ─────────────────────────────────────────────────────────────────────────────

class CounterApp:
    """
    GUI 앱의 기능을 담당하는 클래스.
    Tkinter의 여러 위젯(라벨, 버튼)을 조합해 카운터 UI를 만듭니다.
    """

    def __init__(self, root):
        """
        생성자(Constructor): 프로그램이 시작할 때 UI를 초기화합니다.
        
        매개변수:
            root (tk.Tk): Tkinter 윈도우 객체 (모든 위젯의 부모)
        """

        
        
        # [1] 상태 변수: 클릭 횟수를 저장할 변수 초기화
        self.count = 0
        
        # [2] 윈도우 설정: 창의 제목 지정
        root.title("카운터 예제")
        
        # ─────────────────────────────────────────────────────────────────────
        # 【위젯 1: Label (라벨)】
        # 용도: 현재 카운트 값을 화면에 표시
        # ─────────────────────────────────────────────────────────────────────
        self.label = tk.Label(
            root,                           # 부모 윈도우
            text=f"Count: {self.count}",    # 초기 표시 텍스트 (현재: "Count: 0")
            font=("Arial", 16)              # 폰트: Arial, 크기 16
        )
        # pack(): 위젯을 윈도우에 배치하는 방식 (자동 정렬)
        self.label.pack(padx=20, pady=10)   # padx: 좌우 여백, pady: 상하 여백
        
        # ─────────────────────────────────────────────────────────────────────
        # 【위젯 2: Button (버튼)】
        # 용도: 사용자가 클릭해서 카운트를 증가시킴
        # ─────────────────────────────────────────────────────────────────────
        btn = tk.Button(
            root,                           # 부모 윈도우
            text="Increment",               # 버튼 위에 표시될 텍스트
            command=self.increment,         # 🔑 클릭 시 호출할 함수 (이벤트 바인딩)
            width=12                        # 버튼의 너비
        )
        btn.pack(padx=20, pady=10)

    def increment(self):
        """
        버튼 클릭 시 실행될 함수 (콜백 함수).
        
        역할:
            1. count 변수를 1 증가
            2. label의 텍스트를 새로운 값으로 업데이트
        """
        
        # [1] count 증가
        self.count += 1
        
        
        # [2] 라벨 텍스트 변경: .config() 메서드 사용
        #     (GUI에 반영되려면 명시적으로 위젯을 업데이트해야 함)
        self.label.config(text=f"Count: {self.count}")


# ─────────────────────────────────────────────────────────────────────────────
# 【프로그램 진입점】
# ─────────────────────────────────────────────────────────────────────────────

if __name__ == "__main__":
    # [1] 메인 윈도우 객체 생성
    #     Tk(): Tkinter 애플리케이션의 최상위 윈도우
    root = tk.Tk()
    
    # [2] CounterApp 클래스 인스턴스 생성 (UI 초기화)
    app = CounterApp(root)
    
    # [3] 이벤트 루프 시작: 윈도우가 닫힐 때까지 계속 실행
    #     mainloop(): 사용자 입력(클릭)을 기다리고 응답하는 무한 루프
    root.mainloop()


# ═════════════════════════════════════════════════════════════════════════════
# 【핵심 개념 정리】
# ═════════════════════════════════════════════════════════════════════════════
#
# 1️⃣ 윈도우 (Window)
#    - GUI 앱의 최상위 컨테이너
#    - tk.Tk()로 생성
#    - 모든 위젯은 이 윈도우 안에 배치됨
#
# 2️⃣ 위젯 (Widget)
#    - GUI의 구성 요소: 버튼, 라벨, 텍스트 입력창 등
#    - 부모 윈도우를 첫 번째 매개변수로 받음
#    - 이 예제에서 사용한 위젯:
#      • Label: 텍스트 표시
#      • Button: 클릭 가능한 버튼
#
# 3️⃣ 배치 (Layout)
#    - 위젯을 윈도우에 배치하는 방식
#    - pack(): 자동 정렬 (간단하고 직관적)
#    - grid(): 격자 배치 (복잡한 레이아웃에 유용)
#    - place(): 절대 위치 지정 (세밀한 제어 가능)
#
# 4️⃣ 이벤트 (Event)
#    - 사용자의 행동: 마우스 클릭, 키 입력 등
#    - 이 예제: 버튼 클릭 = 이벤트
#
# 5️⃣ 콜백 (Callback)
#    - 이벤트 발생 시 자동으로 호출되는 함수
#    - command=self.increment ← 버튼 클릭 시 increment() 함수 실행
#
# 6️⃣ 상태 업데이트
#    - GUI를 변경하려면 .config() 메서드 사용
#    - self.label.config(text="새로운 텍스트")
#
# ═════════════════════════════════════════════════════════════════════════════

